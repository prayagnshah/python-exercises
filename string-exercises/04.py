str1 = "PyNaTive"  ##given string
##iterating the string
for c in str1:
    if c.islower():   ##using the tool islower to print lowercase letters 
        print(c, end="")
        
for c in str1:
    if c.isupper():  ##using the tool isupper to print uppercase letters
        print(c, end="")
    
    